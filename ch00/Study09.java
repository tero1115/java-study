package ch00;

public class Study09 {
    public static void main(String[] args) {
        int num1 = 1;
        // 기본형 타입을 객체처럼 사용할 수 있다
        // 래퍼 타입 (Wrapper)
        // 래퍼 타입은 메소드가 있고 null을 넣을 수 있다
        Integer num2 = 1;

        System.out.println(num2);

        // num1 = null;
        num2 = null;
        
    }
}
